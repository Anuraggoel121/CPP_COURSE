#include<iostream>
using namespace std;
int main(){
    int a=4, b=5;
    cout<<"these are the arithmetic operator in C++"<<endl;
    // arithmetic operator
    cout<<"the value of a+b "<<a+b<<endl;
    cout<<"the value of a-b "<<a-b<<endl;
    cout<<"the value of a*b "<<a*b<<endl;
    cout<<"the value of a/b "<<a/b<<endl;
    cout<<"the value of a%b "<<a%b<<endl;
    cout<<"the value of a++ "<<a++<<endl;
    cout<<"the value of a-- "<<a--<<endl;
    cout<<"the value of ++a "<<++a<<endl;
    cout<<"the value of --a "<<--a<<endl;
    cout<<endl;
    // comparison operator
    cout<<"these are the comparison operator "<<endl;
    cout<<"the value of a==b "<<(a==b)<<endl;
    cout<<"the value of a!=b "<<(a!=b)<<endl;
    cout<<"the value of a<b "<<(a<b)<<endl;
    cout<<"the value of a>b "<<(a>b)<<endl;
    cout<<"the value of a<=b "<<(a<=b)<<endl;
    cout<<endl;
    // LOGICAL OPERATOR
    cout<<"the value of ((a+b)&& (a>b)) logical operator is"<<((a+b)&& (a>b))<<endl;
    cout<<endl;
    cout<<"the value of ((a+b)|| (a>b)) logical operator is"<<((a+b)|| (a>b))<<endl;
    return 0;
 }
